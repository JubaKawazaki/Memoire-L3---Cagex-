                    <strong> Indemnisation :</strong><br>

                    A partir de la demande d’intervention, et, au plus tard, dans les 6 mois, et ce,
                    après vérification que l’assuré ait accompli convenablement ses obligations vis-à-vis de son client, ce dernier est indemnisé.<br>

                    <strong>Coût de la garantie :</strong><br>

                    En contrepartie des garanties qu’elle octroie à l’Assuré, ce dernier versera à la CAGEX une prime d’assurance calculée sur la base du chiffre d’affaires prévisionnel réalisable.<br>

                    La prime payable sur le chiffre d’affaires prévisionnel constitue une provision, réajustable, en fin d’exercice d’assurance, sur la base du chiffre d’affaires réel.<br>

                    La prime provisionnelle est payable trimestriellement.<br>

                    La détermination du taux de prime est en fonction des paramètres suivants :<br>

                    - Le chiffre d’affaires assurable,<br>

                    - la Qualité des acheteurs,<br>

                    - les Garanties de paiement,<br>

                    - la Durée du crédit<br>

                    <strong>Avantage :</strong><br>

                    • Assainissement du portefeuille de créances,<br>

                    • Accès aux financements bancaires,<br>

                    • Suivi des créances et surveillance du portefeuille par la CAGEX,<br>

                    • Acquisition de nouvelles parts de marché,<br>

                    • Augmentation du chiffre d’affaires par l’octroi des crédits aux clients,<br>

                    • Sérénité dans les activités commerciales de l’assuré,<br>

                    • Indemnisation substantielle et équilibre de la trésorerie de l’assuré.<br>

                    S'agissant d'un chiffre d'affaires prévisionnel, l'assuré est tenu à payer à la signature de la police, un minimum de
                    prime trimestrielle avec réajustement de la prime dur régulièrement et au plus tard en fin d'exercice, s'y ajoute une
                    commission trimestrielle au titre de la surveillance du risque et une participation forfaitaire au frais d'ouverture,
                    d'étude et de gestion du dossier.

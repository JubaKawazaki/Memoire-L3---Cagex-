        <h1 class="title is-1">NOS SERVICES</h1>

        <div class="columns is-multiline">
            <div class="column">
                <strong>
                    <p class="subtitle is-3">Assurance Crédit Globalliance</p>
                </strong>
                <br>
                <center class="box">
                    <strong>Principe de Garantie:</strong><br>

                    Cette Police consiste à couvrir les risques de non-paiement, aux échéances convenues, du montant de factures,
                    dans le cadre de transaction commerciales de biens et/ou de services intégrant
                    dans le processus de leur facturation ou élaboration une valeur ajoutée nationale.<br>
                    Il s’agit de transactions mettant en relation Fournisseur et des Clients ayant la qualité de commerçants,
                    régulièrement immatriculés au Centre National du Registre du Commerce.<br>
                    L’Assurance Globalliance est une formule d’assurance de standard international,
                    expérimentée par la COFACE, en Europe, et lancée par la CAGEX en Algérie.<br>

                    <strong>Risques couverts : </strong><br>

                    La police Globale couvre le risque de crédit, soit le non-paiement de la créance résultant de l’insolvabilité du débiteur dans les cas suivants :<br>

                    - Avènement d’un jugement prononçant sa faillite et opposable à l'ensemble judiciaire.<br>

                    - Arrangement amiable préventif de la faillite et opposable à l’ensemble des créances.<br>

                    -Non-paiement total au partiel de la dette assurée à l'intérieur du délai de trois(03) mois suivant la réception par la CAGEX d'une demande d'intervention émise par l'assuré.<br>

                    <strong>Quotité garantie :</strong><br>

                    La CAGEX indemnisera l’assuré selon les pourcentages fixés aux conditions particulières et applicables aux montants de la créance telle que facturée au débiteur.<br>

                    La quotité assurée est de <span style="color:red;"> 90% </span>.<br>

                    Elle peut porter sur les créances Hors Taxes ou les créances TTC lorsque la TVA est due à la facturation et n’est pas récupérable L’assuré s’engage à maintenir à
                    sa charge exclusive la fraction non garantie du risque, soit <span style="color:red;"> 10% </span>. Toutefois, la CAGEX prend en charge le recours contre le débiteur défaillant pour la totalité de la créance.<br>
                    En cas de récupération totale, la CAGEX reverse à l’Assuré la proportion de la quotité non assurée, déduction faite des frais nécessités pour sa récupération.<br>

                    <strong>Fonctionnement de la garantie :</strong><br>

                    L’assuré remplit le formulaire d’assurance prévu par la procédure en vigueur, incluant la liste des acheteurs que celui-ci entend intégrer dans la police d’assurance.<br>
                    Chaque acheteur fera l’objet d’un agrément de la part de la CAGEX qui mentionnera le montant du découvert assuré, la durée de validité et les conditions de payement.<br>

                    L’agrément est délivré après analyse du risque « acheteur ».<br>

                    Lorsqu’à l’échéance, une créance n’aura pas été payée, l’assuré doit informer la CAGEX de la situation par l’envoi à cette dernière <span style="color:blue;"> une Déclaration de Menace de Sinistre - DMS.</span><br>

                    Simultanément ou un mois après la déclaration de la DMS, l’assuré adressera à la CAGEX une demande d’intervention ou Mandat, qui constitue le point de départ du délai constitutif de sinistre entrainant l’exigibilité du sinistre.<br>


    <h1 class="title is-1" style="margin-left:2rem ;">NOS SERVICES</h1>

    <div class="columns is-multiline" style="margin-left:2rem ; margin-right:2rem;">
        <div class="column">
            <strong>
                <p class="subtitle is-3">Assurance Crédit- Police Globale</p>
            </strong>
            <br>
            <center class="box">
                <strong>Principe de Garantie:</strong><br>

                La police globale est destinée aux exportateurs de biens et de services, entretenant des contrats d'affaires ou ventes répétitives à
                l 'étranger assortis ou non durées de crédit. Cette police garantie globalement les ventes réalisées à l'exportation. De ce fait
                l'assuré confie à la CAGEX l'ensemble de son chiffre d'affaires à l'exportation.<br>
                <strong>Risques couverts : </strong><br>

                La police globale couvre le risque de crédit, soit le non-paiement de la créance résultant de:<br>

                - l'insovabilité ou carence d'un acheteur privé au titre du risque commercial.<br>

                - la survenance d'un risque politique, catastrophe naturelle, et non transfert ou de la carence d'un acheteur public, au titre du risque politique.<br>

                <strong>Garanties complémentaires :</strong><br>

                les garanties complémentaires font l'objet d'un avenant au contrat d'assurance, à la demande de l'assuré et permettent de couvrir :<br>

                - le risque de fabrication.<br>

                - le risque d'interruption de marché.<br>

                <strong>Quotité garantie :</strong><br>

                Le taux de couverture, appelé quotité, est fixé à :<br>

                <span style="color:red;"> - 80% </span> du montant de l'impayé au titre risque commer­cial.<br>

                <span style="color:red;"> -90% </span> du montant de l'impayé au titre du risque politique .<br>

                <strong>Fonctionnement de la garantie :</strong><br>

                Agrément des acheteurs :<br>

                Avant de traiter avec un acheteur étranger, l’assuré formule une demande d’agrément préalable sur la base d’un imprimé fourni par
                la Compagnie, appelé « demande d’agrément à l’Export ».<br>

                La demande d’agrément est suivie d’une enquête auprès des bases de données disponibles. Lorsque l’enquête est positive, la Compagnie
                délivre à l’Assuré une décision d’agrément qui indique le montant du découvert garanti sur l’acheteur étranger,
                la durée de la validité et les conditions de paiement requises.<br>

                Tout impayé doit être signalé à la Compagnie le plus tôt possible et dans la limite de soixante (60) jours de la date d’échéance
                initiale ou prorogée sur la base de l’imprimé intitulé « Déclaration de Menace de Sinistre » appelé aussi « DMS ».<br>
                <br>
                <br>

                <div class="column">
                    <br>
                    <br>
                </div>
            </center>
            <div class="column">
                <strong>
                    <p class="subtitle is-3">Assurance Crédit- Police Individuelle</p>
                </strong>
                <br>
                <center class="box">
                    <strong>Principe de Garantie:</strong><br>

                    La police individuelle couvre les opérations ponctuelles d'exportation de biens et services, assortie ou non d'une durée de crédit.<br>

                    <strong>Risques couverts : </strong><br>

                    La police individuelle garantit les exportateurs contre le risque de non-paiement des créances détenues sur l'acheteur étranger
                    et résultant d'événements tels que:<br>

                    - Insolvabilité ou carence d'un acheteur privé au titre du risque commercial.<br>

                    - la survenance d'un risque politique, catastrophe naturelle, de non transfert ou de la carence d'un acheteur public,
                    au titre du risque politique.<br>

                    - la survenance d'un risque politique, catastrophe naturelle, et non transfert ou de la carence d'un acheteur public,
                    au titre du risque politique.<br>

                    <strong>Garanties complémentaires :</strong><br>

                    En sus de police individuelle, l'exportation peut se couvrir également, dans le cadre d'une police complémentaire de l'événement ci-après:<br>

                    - le risque de fabrication .<br>

                    - le risque d'interruption de marché.<br>

                    <strong>Quotité garantie :</strong><br>

                    Le taux de couverture, appelé quotité, est fixé à :<br>

                    <span style="color:red;"> - 80% </span> du montant de l'impayé au titre risque commer­cial.<br>

                    <span style="color:red;"> -90% </span> du montant de l'impayé au titre du risque politique .<br>

                    <strong>Fonctionnement de la garantie :</strong><br>

                    Durant la négociation du contrat commercial, l’entreprise doit déposer auprès de la Compagnie une demande de
                    garantie en indiquant, outre le pays de destination, les coordonnées de l'acheteur concerné, la durée du crédit
                    éventuelle, le mode de paiement prévu et le montant du découvert demandé.<br>

                    Lorsque la négociation commerciale aboutit, le contrat d’assurance est signé, après accord sur les conditions
                    d’assurance.<br>

                    La prime d’assurance est payable au comptant, à la signature du contrat d’assurance. Seul le payement de la prime
                    entraine la mise en œuvre des garanties d’assurance du contrat.<br>
                </center>
            </div>



        </div>

        <div class="column ">
            <center class="box">
                <figure class="image is-512x512">
                    <img src="img/drift.png">
                </figure> <br>
                La DMS peut être accompagnée d’une demande d’intervention qui constitue le point de départ du délai constitutif du
                sinistre.
                Cette demande d’intervention peut être différée, mais, elle doit être impérativement transmise à la Compagnie dans un
                délai
                de Trente ( <span style="color: red;">30</span> ) jours, au plus tard, de l’expiration du délai de <span style="color: red;"> 60 </span> jours.<br>

                La remise de la demande d’intervention fait courir le délai constitutif de sinistre prévu dans la police d’assurance.<br>

                <strong> Indemnisation :</strong><br>

                A partir de la demande d'intervention et après vérification que l'assuré ait accompli convenablement ses obligations
                vis-à-vis de l'acheteur, L'indemnisation intervient:<br>

                <strong><span style="color: midnightblue;">Pour le risque commercial :</span></strong><br>

                - Deux (<span style="color: red;"> 02</span> ) mois, au plus tard, pour les créances d'un montant inferieur à trois
                cent mille dinars <br>(<span style="color: red;"> 300.000 DA</span> ).<br>

                - Cinq (<span style="color: red;"> 05</span> ) mois au plus tard, pour les autres créances. Pour le risque politique:<br>

                - Au plus tard six (<span style="color: red;"> 06</span> ) mois.<br>

                <strong>Coût de la garantie :</strong><br>

                Le coût de la garantie est calculé au prorata du chiffre d'affaires assuré. La détermination des taux de prime est
                fonction des paramètres suivants :<br>
                <p>
                    - le volume du chiffre d'affaires à réaliser. <br>

                    - Qualité de l'acheteur. <br>

                    - garantie de paiement retenus.<br>

                    - durée du crédit.<br>

                    - qualité du pays de l'acheteur.<br>
                </p>
                S'agissant d'un chiffre d'affaires prévisionnel, l'assuré est tenu à payer à la signature de la police, un minimum de
                prime trimestrielle avec réajustement de la prime dur régulièrement et au plus tard en fin d'exercice, s'y ajoute une
                commission trimestrielle au titre de la surveillance du risque et une participation forfaitaire au frais d'ouverture,
                d'étude et de gestion du dossier.
            </center><br>


                    Aprés que l'assuré ait remplit convenablement ses obligations vis-à-vis de l'acheteur et suite à la surve­nance d'un impayé,
                    l'indemnisation intervient six (06) mois après la réception de la demande d'intervention.<br>

                    L'indemnité est calculée par application de la garantie.<br>
                    <strong>Coût de la garantie : </strong><br>


                    Le coût de la garantie ou le taux de prime dû par l'assuré est calculé, en fonction de la nature
                    du risque et des paramètres suivants:<br>

                    - le montant du chiffre d'affaires assuré.<br>

                    -la qualité de l'acheteur.<br>

                    - la durée de crédit.<br>

                    -qualité du pays de destination.<br>

                    La prime est due à la signature d'assurance.<br>

                    s'y ajoute une commission trimestrielle au titre de la surveillance du risque et
                    une participation forfaitaire aux frais d'ouverture d'étude et de gestion du dossier.<br>

                    Avantages de la Police Individuelle :

                    A la demande de l‘assuré, le droit aux indemnités peut être transféré à une banque.

                    L’assurance de la Compagnie constitue une garantie de premier choix pour l’accès au financement bancaire.

                    Elle peut être utilisée dans les ventes adossées à des marchés réalisables à moyen terme et payables par tranche.

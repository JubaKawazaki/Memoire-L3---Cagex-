                <center class="box">
                    <strong>Principe </strong><br>

                    Ce fardeau peut être substantiellement allégé par le recours au service de recouvrement de la CAGEX.<br>

                    Le recouvrement du produit de leur vente (créances) tant au plan national qu'international,
                    impose souvent aux entreprise concernées d'engager des actions couteuses disproportion­ nées aux résultats qu'elles permettent d'atteindre.<br><br><br><br><br>

                    <strong>Fonctionnement : </strong><br>

                    Le recouvrement de créances par la CAGEX se fait sur mandat express du créancier.<br> Le débiteur peut être national ou un étranger.<br>

                    Les montants recouvrés par la CAGEX sont versés aux créanciers mandants sous déduction de la rému­ nération due à la CAGEX ainsi que des frais qu'elle aura engagés dans cette action.<br><br><br>

